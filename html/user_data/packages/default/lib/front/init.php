<?php
/*
# デザイン側共通の初期化用ファイル

▼ ■■■■■
▲ ■■■■■
で囲まれている部分はサイト毎に設定が必要です。
*/
$server_name = $_SERVER['SERVER_NAME'];
$request_uri = $_SERVER['REQUEST_URI'];



//----------------------------------------------------------------------------------------------------
## サイト名を定義する
//----------------------------------------------------------------------------------------------------
//▼ ■■■■■
define(SITE_NAME, '格安モバイルルーター比較');
//▲ ■■■■■



//----------------------------------------------------------------------------------------------------
## サイトルートディレクトリを定義する
//----------------------------------------------------------------------------------------------------
//▼ ■■■■■
define(SITE_ROOT_DIR, '/');//末尾は半角スラッシュで終わる
//▲ ■■■■■



//----------------------------------------------------------------------------------------------------
## 各ページへのリンクを定義する
//----------------------------------------------------------------------------------------------------
define(TOP_URL, SITE_ROOT_DIR);
//▼ ■■■■■
//define(ページの識別子名_URL, SITE_ROOT_DIR.'頭にスラッシュなしのディレクトリ名、または、ファイル名')
define(COMPANY_URL, SITE_ROOT_DIR.'company.php');
define(PRIVACY_POLICY_URL, SITE_ROOT_DIR.'privacypolicy.php');
define(ENTRY_FORM_URL, SITE_ROOT_DIR.'form-entry/');
define(INQUIRY_FORM_URL, SITE_ROOT_DIR.'form-inquiry/');
//▲ ■■■■■



//----------------------------------------------------------------------------------------------------
## テストサイトかどうかを定義する
//----------------------------------------------------------------------------------------------------
if(strpos(SITE_ROOT_DIR, '/t/') === false){
	//テストサイトでない場合
	define(ABTEST_FLAG, false);
}
else{
	//テストサイトである場合
	define(ABTEST_FLAG, true);
}



//----------------------------------------------------------------------------------------------------
## <meta property="og:foo">を定義する
//----------------------------------------------------------------------------------------------------
if(!ABTEST_FLAG){
	//テストサイトでない場合
	define(OG_URL_CONTENT, 'https://'.$server_name.$request_uri);
	define(OG_IMAGE_CONTENT, 'https://'.$server_name.SITE_ROOT_DIR.'img/og-image.png');
}
else{
	//テストサイトである場合
	define(OG_URL_CONTENT, str_replace('/t/', '/', 'https://'.$server_name.$request_uri));
	define(OG_IMAGE_CONTENT, str_replace('/t/', '/', 'https://'.$server_name.SITE_ROOT_DIR.'img/og-image.png'));
}



//----------------------------------------------------------------------------------------------------
## <meta name="viewport" content="width=9999">を定義する
//----------------------------------------------------------------------------------------------------
//▼ ■■■■■
define(META_VIEWPORT_WIDTH, 1100);
//▲ ■■■■■



//----------------------------------------------------------------------------------------------------
## <head>開始タグの直後に挿入する文字列を定義する
//----------------------------------------------------------------------------------------------------
//▼ ■■■■■
$head_top = <<<HTML
HTML;
//▲ ■■■■■



//----------------------------------------------------------------------------------------------------
## <head>終了タグの直前に挿入する文字列を定義する
//----------------------------------------------------------------------------------------------------
//▼ ■■■■■
$head_end = <<<HTML
HTML;
//▲ ■■■■■



//----------------------------------------------------------------------------------------------------
## <body>開始タグの直後に挿入する文字列を定義する
//----------------------------------------------------------------------------------------------------
//▼ ■■■■■
$body_top = <<<HTML
HTML;
//▲ ■■■■■



//----------------------------------------------------------------------------------------------------
## <body>終了タグの直前に挿入する文字列を定義する
//----------------------------------------------------------------------------------------------------
//▼ ■■■■■
$body_end = <<<HTML
HTML;
//▲ ■■■■■



?>